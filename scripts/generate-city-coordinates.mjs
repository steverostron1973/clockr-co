const src = fs.readFileSync('src/data/worldClockSearchCities.ts', 'utf8');
const re = /city: (?:'([^']+)'|"([^"]+)"), country: '([^']+)'/g;
const missing = [];
let m;
while ((m = re.exec(src))) {
	const key = `${m[1] || m[2]}|${m[3]}`;
	if (!COORDS[key]) missing.push(key);
}

if (missing.length) {
	console.error('Missing coordinates:', missing);
	process.exit(1);
}

const lines = Object.entries(COORDS)
	.sort(([a], [b]) => a.localeCompare(b))
	.map(([key, [lat, lng]]) => {
		const escaped = key.includes("'") ? `"${key}"` : `'${key}'`;
		return `\t${escaped}: { lat: ${lat}, lng: ${lng} },`;
	});

const out = `export interface CityCoordinates {
\tlat: number;
\tlng: number;
}

export const CITY_COORDINATES: Record<string, CityCoordinates> = {
${lines.join('\n')}
};

export function getCityCoordinates(city: string, country: string): CityCoordinates {
\tconst key = \`\${city}|\${country}\`;
\tconst coords = CITY_COORDINATES[key];
\tif (!coords) {
\t\tthrow new Error(\`Missing coordinates for \${city}, \${country}\`);
\t}
\treturn coords;
}
`;

fs.writeFileSync('src/data/cityCoordinates.ts', out);
